package logica;

public class Nave {
	
	private String nome;
	private int tamanho;
	private int x;
	private int y;
	private boolean afundado;
	
	public Nave(String nome, int tamanho, int x, int y) {
		super();
		this.nome = nome;
		this.tamanho = tamanho;
		this.x = x;
		this.y = y;
		this.afundado = false;
	}

	

}
